// cos'e una stringa?
// una stringa è un insieme di caratteri
var stringa = " ";

// delimiatatori / definizione
// una stringa è delimitata da:
// - apici singoli (')
// - o doppi ("")
// - o backtick (`) (alt+96)

// non ha una lunghezza massima (Infinity)
// nona ha una lunghezza minima (0)
var a = '';

// concatenazione (+)
var b = 'ciao';
var c = 'mondo';
var d = b + ' ' + c;

// interpolazione
var e = `${b} ${c}`;

// apostrofi e virgolette
var f = "ciao 'mondo'";
var g = 'ciao "mondo"';
var h = 'ciao \'mondo\''; // escape/escaping
var i = "ciao \"mondo\""; // escape/escaping
var l = `ciao 'mondo'`;
var m = `ciao "mondo"`;